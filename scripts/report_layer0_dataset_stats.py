#!/usr/bin/env python3
"""
Download acquisition-registry datasets and print coarse size/row/line stats.

This script is read-only with respect to upstream source data. The historical
`--write-doc` mode is retained as a compatibility flag, but now fails fast with
guidance because `docs/DATASET_ACQUISITION_LAYER0.md` was retired.

Usage (repo root):
  python scripts/report_layer0_dataset_stats.py
  python scripts/report_layer0_dataset_stats.py --json
"""

from __future__ import annotations

import argparse
import csv
import io
import json
import re
import sys
import zipfile
from pathlib import Path
from typing import Any

REPO = Path(__file__).resolve().parent.parent
if str(REPO / "src") not in sys.path:
    sys.path.insert(0, str(REPO / "src"))

from study_query_llm.datasets.acquisition import download_acquisition_files, fetch_url
from study_query_llm.datasets.source_specs.registry import ACQUIRE_REGISTRY


def _xlsx_sheet1_row_count(data: bytes) -> int:
    """Approximate data row count from first worksheet XML (no openpyxl)."""
    with zipfile.ZipFile(io.BytesIO(data)) as zf:
        names = sorted(
            n for n in zf.namelist() if re.match(r"xl/worksheets/sheet\d+\.xml$", n)
        )
        if not names:
            return 0
        xml = zf.read(names[0]).decode("utf-8", errors="replace")
    # Each <row ...> is one sheet row; subtract 1 if first row is header (heuristic: keep as total rows)
    return len(re.findall(r"<row[^>]*>", xml))


def _csv_stats(data: bytes) -> dict[str, Any]:
    text = data.decode("utf-8", errors="replace")
    f = io.StringIO(text)
    reader = csv.reader(f)
    rows = list(reader)
    n = len(rows)
    widths = [len(max(row, key=len)) if row else 0 for row in rows[:5000]]
    chars = sum(sum(len(c) for c in row) for row in rows[:5000])
    sample = n if n <= 5000 else 5000
    return {
        "rows": n,
        "cols_max": max((len(r) for r in rows), default=0),
        "avg_chars_per_row_sample": round(chars / sample, 1) if sample else 0,
        "max_cell_chars_sample": max(widths) if widths else 0,
    }


def _line_stats(data: bytes) -> dict[str, Any]:
    text = data.decode("utf-8", errors="replace")
    lines = [ln for ln in text.splitlines() if ln.strip()]
    if not lines:
        return {"non_empty_lines": 0, "avg_line_chars": 0, "max_line_chars": 0}
    lens = [len(ln) for ln in lines]
    return {
        "non_empty_lines": len(lines),
        "avg_line_chars": round(sum(lens) / len(lens), 1),
        "max_line_chars": max(lens),
    }


def _slug_stats(slug: str) -> dict[str, Any]:
    cfg = ACQUIRE_REGISTRY[slug]
    specs = cfg.file_specs()
    files = download_acquisition_files(specs, fetch=fetch_url)
    total_bytes = sum(f.byte_size for f in files)
    per_file: list[dict[str, Any]] = []
    for f in files:
        rel = f.relative_path
        ext = Path(rel).suffix.lower()
        row: dict[str, Any] = {
            "relative_path": rel,
            "byte_size": f.byte_size,
            "sha256_prefix": f.sha256[:12],
        }
        if ext == ".csv":
            st = _csv_stats(f.data)
            row.update(
                rows=st["rows"],
                cols=st["cols_max"],
                avg_chars_row=st["avg_chars_per_row_sample"],
            )
        elif ext == ".xlsx":
            row["sheet1_row_tags"] = _xlsx_sheet1_row_count(f.data)
        elif ext in (".txt", ".md"):
            st = _line_stats(f.data)
            row.update(
                non_empty_lines=st["non_empty_lines"],
                avg_line_chars=st["avg_line_chars"],
                max_line_chars=st["max_line_chars"],
            )
        per_file.append(row)
    return {
        "slug": slug,
        "source": cfg.source_metadata(),
        "file_count": len(files),
        "total_bytes": total_bytes,
        "files": per_file,
    }


def _markdown_table(results: list[dict[str, Any]], measured_at: str) -> str:
    lines = [
        "",
        "## Measured Layer 0 statistics (auto-generated)",
        "",
        f"Regenerated by `scripts/report_layer0_dataset_stats.py`. Last run (UTC): `{measured_at}`. "
        "Counts are from pinned upstream bytes at fetch time; Zenodo/GitHub may change if pins change.",
        "",
        "| Slug | Files | Total bytes | Row / line summary |",
        "|------|-------|---------------|---------------------|",
    ]
    for r in results:
        slug = r["slug"]
        fc = r["file_count"]
        tb = r["total_bytes"]
        parts: list[str] = []
        for pf in r["files"]:
            rel = pf["relative_path"]
            if "rows" in pf:
                parts.append(f"`{rel}`: {pf['rows']} CSV rows, {pf['cols']} cols")
            elif "sheet1_row_tags" in pf:
                parts.append(
                    f"`{rel}`: ~{pf['sheet1_row_tags']} sheet rows (XML `<row>` count; includes header row)"
                )
            elif "non_empty_lines" in pf:
                parts.append(
                    f"`{rel}`: {pf['non_empty_lines']} non-empty lines "
                    f"(avg {pf['avg_line_chars']} chars, max {pf['max_line_chars']})"
                )
        summary = "; ".join(parts) if parts else "—"
        lines.append(f"| `{slug}` | {fc} | {tb:,} | {summary} |")
    lines.extend(
        [
            "",
            "**Notes:**",
            "",
            "- **AuSeM CSV `rows`:** includes the header row; column counts are max columns seen in that file.",
            "- **SemEval `.txt` `non_empty_lines`:** physical lines; some gold files are a single long line of many tab-separated records.",
            "- **`sources_v2.xlsx`:** row count is the number of `<row>` elements in the first worksheet XML (includes header); approximate if Excel uses sparse rows.",
            "",
        ]
    )
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description="Report Layer 0 dataset sizes and coarse counts")
    parser.add_argument(
        "--write-doc",
        action="store_true",
        help="Deprecated compatibility flag; use stdout output and redirect to a file if needed.",
    )
    parser.add_argument(
        "--json",
        action="store_true",
        help="Print full JSON stats to stdout",
    )
    args = parser.parse_args()

    if args.write_doc:
        print(
            "[DEPRECATED] --write-doc is no longer supported because "
            "docs/DATASET_ACQUISITION_LAYER0.md was retired during pipeline cutover.",
            file=sys.stderr,
        )
        print(
            "Use stdout output (default or --json) and redirect to your target file. "
            "See docs/DATA_PIPELINE.md for canonical acquisition documentation.",
            file=sys.stderr,
        )
        return 2

    from datetime import datetime, timezone

    measured_at = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%MZ")
    results: list[dict[str, Any]] = []
    for slug in sorted(ACQUIRE_REGISTRY.keys()):
        print(f"[fetch] {slug} …", file=sys.stderr)
        results.append(_slug_stats(slug))

    if args.json:
        print(json.dumps({"measured_at": measured_at, "datasets": results}, indent=2))
    else:
        print(_markdown_table(results, measured_at))

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
